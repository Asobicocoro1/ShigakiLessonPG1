#### プレイヤの配置
- **プレイヤオブジェクトをシーンに配置しよう**
  - シーンには、ゲームで操作するキャラクター（プレイヤ）を置く必要があります。
  - Unityの「ヒエラルキー」ウィンドウで、右クリックして「3Dオブジェクト」→「Cube」を選びます。
