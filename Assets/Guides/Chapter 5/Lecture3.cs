- **シーンに背景画像を追加しよう**
  - ゲームの背景を設定すると、見た目が良くなります。
  - 「ヒエラルキー」ウィンドウで右クリックして、「UI」→「Image」を選びます。
  - このImageを背景として使います。名前を「Background」に変えましょう。
