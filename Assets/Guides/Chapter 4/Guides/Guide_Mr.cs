using UnityEngine;

/*
  ### UnityのMesh Rendererのインスペクターの中身について

Mesh Renderer（メッシュレンダラー）は、3Dオブジェクトを画面に表示するためのコンポーネントです。
これを使ってオブジェクトにマテリアルを適用し、見た目をコントロールします

#### 1. **Cast Shadows (シャドウをキャスト)**
- **説明**: オブジェクトが影を落とすかどうかを設定します。
- **使い方**: 以下の4つから選びます。

  1. **Off (オフ)**
     - **説明**: オブジェクトが影を落としません。
     - **使い方**: 影を使いたくないときに選びます。

  2. **On (オン)**
     - **説明**: オブジェクトが影を落とします。
     - **使い方**: 通常の設定です。

  3. **Two Sided (両面)**
     - **説明**: オブジェクトの両面が影を落とします。
     - **使い方**: 特殊な場合に使用します。

  4. **Shadows Only (影のみ)**
     - **説明**: オブジェクト自体は表示されず、影だけが表示されます。
     - **使い方**: 特殊なエフェクトに使用します。

#### 2. **Receive Shadows (シャドウを受ける)**
- **説明**: オブジェクトが他のオブジェクトの影を受けるかどうかを設定します。
- **使い方**: チェックを入れると、他のオブジェクトの影が表示されます。

#### 3. **Motion Vectors (モーションベクター)**
- **説明**: モーションベクターを生成して、動きのブラーを表現するかどうかを設定します。
- **使い方**: 以下の3つから選びます。

  1. **Per Object Motion (オブジェクトごとのモーション)**
     - **説明**: オブジェクトごとのモーションベクターを生成します。
     - **使い方**: 通常の設定です。

  2. **Camera Motion Only (カメラのモーションのみ)**
     - **説明**: カメラの動きに基づいてモーションベクターを生成します。
     - **使い方**: カメラの動きが主体の場合に使用します。

  3. **Force No Motion (モーションなし)**
     - **説明**: モーションベクターを生成しません。
     - **使い方**: モーションブラーを使わない場合に選びます。

#### 4. **Materials (マテリアル)**
- **説明**: オブジェクトの見た目を決めるマテリアルを設定します。
- **使い方**: マテリアルをドラッグ＆ドロップして設定します。複数のマテリアルを設定することもできます。

#### 5. **Light Probes (ライトプローブ)**
- **説明**: オブジェクトがライトプローブからの光を受けるかどうかを設定します。
- **使い方**: 以下の3つから選びます。

  1. **Off (オフ)**
     - **説明**: ライトプローブを使用しません。
     - **使い方**: ライトプローブを使わない場合に選びます。

  2. **Blend Probes (ブレンドプローブ)**
     - **説明**: 複数のライトプローブの光をブレンドして使用します。
     - **使い方**: 通常の設定です。

  3. **Use Proxy Volume (プロキシボリュームを使用)**
     - **説明**: プロキシボリュームを使ってライトプローブを設定します。
     - **使い方**: 特殊な場合に使用します。

#### 6. **Reflection Probes (リフレクションプローブ)**
- **説明**: オブジェクトがリフレクションプローブからの反射光を受けるかどうかを設定します。
- **使い方**: 以下の3つから選びます。

  1. **Off (オフ)**
     - **説明**: リフレクションプローブを使用しません。
     - **使い方**: リフレクションプローブを使わない場合に選びます。

  2. **Blend Probes (ブレンドプローブ)**
     - **説明**: 複数のリフレクションプローブの反射光をブレンドして使用します。
     - **使い方**: 通常の設定です。

  3. **Simple (シンプル)**
     - **説明**: シンプルなリフレクションプローブを使用します。
     - **使い方**: 通常の設定です。

#### 7. **Probes Anchor (プローブアンカー)**
- **説明**: ライトプローブやリフレクションプローブの影響を受ける位置を設定します。
- **使い方**: オブジェクトをドラッグ＆ドロップして設定します。通常は設定しません。

### まとめ
Mesh Rendererの設定を理解することで、3Dオブジェクトの見た目や影、反射のコントロールができるようになります。
これにより、ゲーム内でよりリアルな表現を実現できます。色々な設定を試して、自分のゲームに最適な見た目を作り上げましょう。
