/*
 ### Unityのマテリアルのインスペクターの中身について

Unityのマテリアルは、オブジェクトの見た目を決める重要な要素です。
マテリアルを使って色や質感、光沢などを設定することができます。
#### 1. **Shader (シェーダー)**
- **説明**: マテリアルの種類や見た目を決める特別なプログラムのことです。例えば、「Standard」というシェーダーは一般的な物の見た目を作るのに使います。
- **使い方**: リストから選んで、マテリアルの見た目を変えます。

#### 2. **Main Maps (メインマップ)**
- マテリアルの見た目を決めるための画像や色を設定します。

1. **Albedo (アルベド)**
   - **説明**: オブジェクトの基本的な色やテクスチャを設定します。
   - **使い方**: 色を選んだり、画像をドラッグ＆ドロップして、オブジェクトの表面に貼り付けます。

2. **Metallic (メタリック)**
   - **説明**: オブジェクトの金属っぽさを決めます。値が高いほど金属っぽく、低いほどプラスチックっぽく見えます。
   - **使い方**: スライダーを動かして金属の見た目を調整します。

3. **Smoothness (滑らかさ)**
   - **説明**: オブジェクトの表面の滑らかさを決めます。値が高いほどツルツルして光をよく反射し、低いほどザラザラして見えます。
   - **使い方**: スライダーを動かして滑らかさを調整します。

4. **Normal Map (ノーマルマップ)**
   - **説明**: オブジェクトの表面に凹凸をつけて立体的に見せるための特殊な画像です。
   - **使い方**: ノーマルマップ用の画像をドラッグ＆ドロップします。

5. **Height Map (ハイトマップ)**
   - **説明**: オブジェクトの高さ情報を使って、さらに細かい凹凸を表現します。
   - **使い方**: ハイトマップ用の画像をドラッグ＆ドロップします。

#### 3. **Secondary Maps (セカンダリーマップ)**
- 追加の画像や効果を設定します。

1. **Detail Mask (ディテールマスク)**
   - **説明**: 特定の部分にだけ詳細なテクスチャを適用するための画像です。
   - **使い方**: マスク用の画像をドラッグ＆ドロップします。

2. **Detail Albedo x2 (ディテールアルベド x2)**
   - **説明**: オブジェクトの表面に詳細な色やテクスチャを追加します。
   - **使い方**: 詳細テクスチャ用の画像をドラッグ＆ドロップします。

3. **Detail Normal Map (ディテールノーマルマップ)**
   - **説明**: さらに細かい凹凸を追加します。
   - **使い方**: 詳細ノーマルマップ用の画像をドラッグ＆ドロップします。

#### 4. **Tiling (タイル)とOffset (オフセット)**
- テクスチャの繰り返しや位置を設定します。

1. **Tiling (タイル)**
   - **説明**: テクスチャをどのくらい繰り返すかを決めます。
   - **使い方**: 数字を入力してテクスチャの繰り返しを設定します。値を大きくすると繰り返しが増え、小さくすると繰り返しが減ります。

2. **Offset (オフセット)**
   - **説明**: テクスチャの表示位置をずらします。
   - **使い方**: 数字を入力してテクスチャの位置を調整します。

#### 5. **Emission (エミッション)**
- オブジェクトが光を放つように見せる設定です。

1. **Emission Color (エミッションカラー)**
   - **説明**: オブジェクト自体が光を放つ色を設定します。
   - **使い方**: 色を選んで設定します。明るい色を選ぶとオブジェクトが光って見えます。

#### 6. **Rendering Mode (レンダリングモード)**
- オブジェクトの描画方法を設定します。

1. **Opaque (不透明)**
   - **説明**: オブジェクトが完全に不透明になります。
   - **使い方**: リストから「Opaque」を選びます。

2. **Transparent (透明)**
   - **説明**: オブジェクトが透明になります。
   - **使い方**: リストから「Transparent」を選び、アルファ値で透明度を調整します。

3. **Cutout (カットアウト)**
   - **説明**: テクスチャの一部を完全に透明にします。
   - **使い方**: リストから「Cutout」を選び、アルファ値で透明部分を設定します。

### まとめ
マテリアルの設定は、オブジェクトの見た目を大きく変える重要な作業です。色やテクスチャ、光沢や透明度などを調整して、自分のゲームの世界をよりリアルに、美しく作り上げましょう。
